if not localPlayer and SERVER_NUMBER > 100 then
    local lap_elements = {}
    addCommandHandler( "delivery_marker", function( player, cmd, route_id )
    	DestroyTableElements( lap_elements )
    	lap_elements = {}
    	lap_elements.marker_id = 1

    	route_id = tonumber( route_id )
    	if not route_id then return end
    
    
    	for k, point in pairs( CAR_ROUTES[ route_id ] ) do
    		lap_elements[ k .. "m" ] = createMarker( point + Vector3( 0, 0, 1 ) )
    		lap_elements[ k .. "m" ].size = 5

    		lap_elements[ k .. "b" ] = createBlip( point )
    		addEventHandler ( "onMarkerHit", lap_elements[ k .. "m" ], function( element )
                if lap_elements.marker_id ~= k then return end
    			lap_elements.marker_id = lap_elements.marker_id + 1

    			destroyElement( lap_elements[ k .. "m" ] )
    			destroyElement( lap_elements[ k .. "b" ] )
    		end )
    	end
    end )
end

